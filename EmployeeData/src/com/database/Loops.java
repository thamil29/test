package com.database;

public class Loops {

	public static void main(String[] args) {
		int age = 2;
		if (age >= 20) {
			System.out.println("You are an Teenager");
		} else if (age > 35 || age > 45) {
			System.out.println("You are an adult");
		} else {
			System.out.println("You are child or senior citizen");
		}
	}

}
